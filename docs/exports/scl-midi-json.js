// exports/scl-midi-json.js — client-side Blob generators.
// Scala (.scl), Type-1 SMF (.mid), and JSON. No external dependencies.

function download(filename, content, mime = "text/plain") {
  const blob = typeof content === "string" ? new Blob([content], { type: mime }) : new Blob([content], { type: mime });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  setTimeout(() => { a.remove(); URL.revokeObjectURL(url); }, 200);
}

const safe = (s) => s.toLowerCase().replace(/[^a-z0-9]+/g, "_").replace(/^_|_$/g, "");

// ─── .scl (Scala scale file) ──────────────────────────────────
//
// Format: comments start with !, then description, then count, then `count`
// pitch-step lines (cents), with the closing octave line at 1200.000.

export function makeSCL(mela) {
  const cents = mela.pcSet.filter(pc => pc !== 0).map(pc => `${(pc * 100).toFixed(3)}`);
  const lines = [
    `! mela_${String(mela.n).padStart(2, "0")}_${safe(mela.name)}.scl`,
    `! Generated by Melagabra (https://example.invalid/) — Forte ${mela.forte}`,
    `${mela.name} (${mela.nameIAST}) — Mela ${mela.n}`,
    String(cents.length + 1)
  ];
  for (const c of cents) lines.push(c);
  lines.push("1200.000");
  return lines.join("\n") + "\n";
}

export function downloadSCL(mela) {
  const text = makeSCL(mela);
  download(`mela_${String(mela.n).padStart(2, "0")}_${safe(mela.name)}.scl`, text, "application/x-scala");
}

// ─── Master JSON ─────────────────────────────────────────────

export function downloadMasterJson(MELA, ORBITS, EDGES, GRAY_PATH) {
  const out = {
    meta: {
      title: "Melagabra master export",
      paper: "The algebraic structure of the seventy-two Melakartas",
      generatedAt: new Date().toISOString()
    },
    melas: [],
    orbits: ORBITS.map(o => ({ id: o.id, members: o.members, forteSet: o.forteSet })),
    edges: EDGES.map(e => ({ a: e.a, b: e.b, region: e.region, xor: `0x${e.xor.toString(16).toUpperCase().padStart(3,"0")}` })),
    grayPath: GRAY_PATH
  };
  for (let n = 1; n <= 72; n++) {
    const m = MELA[n];
    out.melas.push({
      n: m.n,
      name: m.name,
      nameIAST: m.nameIAST,
      hex: m.hex,
      binary: m.binary,
      chakra: m.chakra,
      position: m.position,
      pcSet: m.pcSet,
      primeForm: m.primeForm,
      forte: m.forte,
      icv: m.icv,
      vivadi: m.vivadi,
      orbitId: m.orbitId,
      orbitMembers: m.orbitMembers,
      janya: m.janya,
      janyaProvenance: m.janyaProvenance
    });
  }
  download("melagabra_master.json", JSON.stringify(out, null, 2), "application/json");
}

// ─── Type-1 SMF MIDI (Gray-walk tour) ────────────────────────
//
// Hand-rolled because the file is small (72 bars) and adding a 25 KB MIDI
// library for one export feels disproportionate.

function vlq(n) {
  const out = [];
  out.unshift(n & 0x7F);
  n >>>= 7;
  while (n > 0) {
    out.unshift((n & 0x7F) | 0x80);
    n >>>= 7;
  }
  return out;
}
function bytesU16(n) { return [(n >> 8) & 0xFF, n & 0xFF]; }
function bytesU32(n) { return [(n >>> 24) & 0xFF, (n >>> 16) & 0xFF, (n >>> 8) & 0xFF, n & 0xFF]; }
function strBytes(s) { return Array.from(s, c => c.charCodeAt(0) & 0xFF); }

function trackChunk(events) {
  // events: array of { delta, bytes }
  const data = [];
  for (const ev of events) {
    data.push(...vlq(ev.delta));
    data.push(...ev.bytes);
  }
  // End of track
  data.push(0x00, 0xFF, 0x2F, 0x00);
  return [...strBytes("MTrk"), ...bytesU32(data.length), ...data];
}

export function makeGrayMidi(GRAY_PATH, M_CROSSING_INDEX, MELA, opts = {}) {
  const ppq = 480;
  const bpm = opts.bpm || 60;
  const microsPerBeat = Math.round(60_000_000 / bpm);

  // Track 0: meta
  const meta = [];
  meta.push({ delta: 0, bytes: [0xFF, 0x51, 0x03, ...bytesU24(microsPerBeat)] });   // tempo
  meta.push({ delta: 0, bytes: [0xFF, 0x58, 0x04, 4, 2, 24, 8] });                    // 4/4
  // Marker per step
  for (let i = 0; i < GRAY_PATH.length; i++) {
    const m = MELA[GRAY_PATH[i]];
    const mark = `${String(i+1).padStart(2,"0")} ${m.name}`;
    const sb = strBytes(mark);
    meta.push({ delta: i === 0 ? 0 : 0, bytes: [0xFF, 0x06, sb.length, ...sb] });
  }
  const metaTrack = trackChunk(meta);

  // Track 1: chord (held 7-note set per step, ch 1, v 64), one bar per step
  const chord = [];
  let firstNote = true;
  const ticksPerBar = ppq * 4;
  for (let i = 0; i < GRAY_PATH.length; i++) {
    const m = MELA[GRAY_PATH[i]];
    const root = 60;
    const notes = m.pcSet.map(pc => root + pc);
    // Note-ons (delta=0 for first, delta=ticksPerBar between bars)
    notes.forEach((n, j) => {
      chord.push({ delta: j === 0 ? (firstNote ? 0 : 0) : 0, bytes: [0x90, n & 0x7F, 64] });
    });
    // Hold for one bar
    chord.push({ delta: ticksPerBar, bytes: [0x80, notes[0] & 0x7F, 0] });
    notes.slice(1).forEach(n => chord.push({ delta: 0, bytes: [0x80, n & 0x7F, 0] }));
    firstNote = false;
  }
  const chordTrack = trackChunk(chord);

  // Track 2: CC1 ramp into M-crossing, CC91 spike at M-crossing
  const cc = [];
  for (let i = 0; i < GRAY_PATH.length; i++) {
    let val;
    if (i <= M_CROSSING_INDEX) val = Math.round((i / M_CROSSING_INDEX) * 127);
    else val = Math.round(127 - ((i - M_CROSSING_INDEX) / (GRAY_PATH.length - 1 - M_CROSSING_INDEX)) * 127);
    cc.push({ delta: i === 0 ? 0 : ticksPerBar, bytes: [0xB0, 1, val & 0x7F] });
    if (i === M_CROSSING_INDEX) cc.push({ delta: 0, bytes: [0xB0, 91, 127] });
  }
  const ccTrack = trackChunk(cc);

  // Header
  const header = [
    ...strBytes("MThd"),
    ...bytesU32(6),
    ...bytesU16(1),
    ...bytesU16(3),
    ...bytesU16(ppq)
  ];

  return new Uint8Array([...header, ...metaTrack, ...chordTrack, ...ccTrack]);
}

function bytesU24(n) {
  return [(n >>> 16) & 0xFF, (n >>> 8) & 0xFF, n & 0xFF];
}

export function downloadGrayMidi(GRAY_PATH, M_CROSSING_INDEX, MELA) {
  const bytes = makeGrayMidi(GRAY_PATH, M_CROSSING_INDEX, MELA);
  download("melagabra_graywalk.mid", bytes, "audio/midi");
}

// ─── Per-mela MIDI palette (7-note clip) ──────────────────────

export function downloadMelaPalette(mela) {
  const ppq = 480;
  const microsPerBeat = Math.round(60_000_000 / 60);
  const meta = [
    { delta: 0, bytes: [0xFF, 0x51, 0x03, ...bytesU24(microsPerBeat)] },
    { delta: 0, bytes: [0xFF, 0x58, 0x04, 4, 2, 24, 8] }
  ];
  const metaTrack = trackChunk(meta);
  const notes = mela.pcSet.map(pc => 60 + pc);
  const ev = [];
  notes.forEach((n, i) => {
    ev.push({ delta: i === 0 ? 0 : 0, bytes: [0x90, n, 80] });
    ev.push({ delta: ppq, bytes: [0x80, n, 0] });
  });
  const noteTrack = trackChunk(ev);
  const header = [
    ...strBytes("MThd"),
    ...bytesU32(6),
    ...bytesU16(1),
    ...bytesU16(2),
    ...bytesU16(ppq)
  ];
  download(`mela_${String(mela.n).padStart(2,"0")}_${safe(mela.name)}_palette.mid`, new Uint8Array([...header, ...metaTrack, ...noteTrack]), "audio/midi");
}
